package com.example.sunilkumarlakkad.travelmate.Model;
public class TaxiFinderResponse {
    private String distance, duration, total_fare, tip_percentage, rate_area;

    public String getDistance() {
        return distance;
    }

    public void setDistance(String distance) {
        this.distance = distance;
    }

    public String getDuration() {
        return duration;
    }

    public void setDuration(String duration) {
        this.duration = duration;
    }

    public String getTotal_fare() {
        return total_fare;
    }

    public void setTotal_fare(String total_fare) {
        this.total_fare = total_fare;
    }

    public String getTip_percentage() {
        return tip_percentage;
    }

    public void setTip_percentage(String tip_percentage) {
        this.tip_percentage = tip_percentage;
    }

    public String getRate_area() {
        return rate_area;
    }

    public void setRate_area(String rate_area) {
        this.rate_area = rate_area;
    }
}
